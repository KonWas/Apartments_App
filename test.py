# Funkcja do rysowania polygonów
def plot_districts(districts):
    fig, ax = plt.subplots()
    for name, polygon in districts.items():
        x, y = polygon.exterior.xy
        ax.plot(x, y, label=name)
    ax.set_title('Dzielnice Wrocławia')
    ax.set_xlabel('Longitude')
    ax.set_ylabel('Latitude')
    ax.legend()
    plt.show()

# Wywołanie funkcji do rysowania
plot_districts(districts)
